package com.langfunOperators;

public class ShiftOperator {

	public static void main(String[] args) {
		int a=69;
		int b=2;
		System.out.println(a<<b);//279
		System.out.println(a>>b);//17
		System.out.println(a>>>b);//17
		//System.out.println(a<<<b);//ce
		
		int x=56;
		int y=3;
		System.out.println(x<<y);//448
		System.out.println(x>>y);//7
	}

}
