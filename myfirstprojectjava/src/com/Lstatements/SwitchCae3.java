package com.Lstatements;
import java.util.Scanner;

public class SwitchCae3 {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter the roll number:");
		int nbr=sc.nextInt();
		
		switch(nbr) {
		case 1 ->{
			System.out.println("Bhumika");
			System.out.println("not a good girl");
		}
		case 2 ->{
			System.out.println("anjali");
			System.out.println("beautiful girl");
		}
		case 3 ->{
			System.out.println("srija");
			System.out.println("my childhood friend");
		}
		}
			}

}
