package com.langfund;

public class TestLiteralasDemo1 {

	public static void main(String[] args) {
		// Decimal literals
		int a1 = 102;
		int a2 = 165;
		int a3 = 112;
		// octal literals starts with 0,and the range 0-->7,base 8
		int a4 = 0123;
		int a5 = 0214;
		int a6 = 0654;
		// hexadecimal starts with 0X ,and the range 0-->9,base 16
		// a=10,b=11,c=12,d=13,e=14
		int a7 = 0Xabde;
		int a8 = 0XDAC;
		int a9 = 0XAb1;
//binary literals starts with 0b,and values only 0's and 1's
		int a10 = 0b010001;
		int a11 = 0b11001;
		int a12 = 0b101101;
		int a13 = 0b101010;
		System.out.println(a1);
		System.out.println(a2);
		System.out.println(a3);

		System.out.println(a4);
		System.out.println(a5);
		System.out.println(a6);

		System.out.println(a7);
		System.out.println(a8);
		System.out.println(a9);

		System.out.println(a10);
		System.out.println(a11);
		System.out.println(a12);
		System.out.println(a13);

	}

}
