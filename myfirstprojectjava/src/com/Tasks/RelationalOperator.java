package com.Tasks;
//it is also known as comparison operator.it gives boolean expressions.

public class RelationalOperator {

	public static void main(String[] args) {
		double a=12.5;
		double b=15.5;
		System.out.println(a==b);//false
		System.out.println(a!=b);//true
		
		System.out.println(a>b);//false
		System.out.println(a<b);//true
		
		System.out.println(a<=b);//true
		System.out.println(a>=b);//false
				
		
		
			}

}
